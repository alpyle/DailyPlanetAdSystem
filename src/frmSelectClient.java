    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        String url = "jdbc:mysql://localhost:3306/";
        String dbName = "ad";
        String driver = "com.mysql.jdbc.Driver";
        String userName = "root";
        String password = "";
        try {
            Class.forName(driver).newInstance();
            Connection conn = DriverManager.getConnection(url + dbName, userName, password);
            Statement st = conn.createStatement();
            ResultSet EmployeesTable = st.executeQuery("Select  clientID, companyName from client");
            cmbClient.removeAllItems();
            cmbClient.addItem("            ");
            while (EmployeesTable.next()) {
                cmbClient.addItem(EmployeesTable.getString("companyName"));
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }        // TODO add your handling code here:
    }//GEN-LAST:event_formWindowOpened

    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        String url = "jdbc:mysql://localhost:3306/";
        String dbName = "ad";
        String driver = "com.mysql.jdbc.Driver";
        String userName = "root";
        String password = "";
        try {
            Class.forName(driver).newInstance();
            Connection conn = DriverManager.getConnection(url + dbName, userName, password);
            Statement st = conn.createStatement();
            ResultSet ClientsTable = st.executeQuery("Select  clientID, companyName from client");
            if (cmbClient.getSelectedIndex() == 0) {
                jLabel1.setText("Please choose a company");
            } else {
                while (ClientsTable.next()) {
                    if (ClientsTable.getString("companyName").equals(cmbClient.getSelectedItem().toString())) {
                        final frmModifyClient toOpen = new frmModifyClient();
                        toOpen.setClientID(ClientsTable.getInt("clientID"));
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                toOpen.setVisible(true);
                            }
                        });
                    }
                }
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_btnSubmitMouseClicked

