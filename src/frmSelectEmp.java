    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost:3306/";
		  String dbName = "ad";
		  String driver = "com.mysql.jdbc.Driver";
		  String userName = "root"; 
		  String password = "";
		  try {
                      Class.forName(driver).newInstance();
                      Connection conn = DriverManager.getConnection(url+dbName,userName,password);
                      Statement st= conn.createStatement();
                      ResultSet EmployeesTable=st.executeQuery("Select  empID, empFirstName, empLastName from employee");
                      comboSelectEmp.removeAllItems();
                      comboSelectEmp.addItem("            ");
                      while (EmployeesTable.next())
                      {
                          comboSelectEmp.addItem(EmployeesTable.getString("empFirstName")+ " " +EmployeesTable.getString("empLastName"));
                      }
                      
                      conn.close();
		  } catch (Exception e) {
		  e.printStackTrace();
                  }
    }//GEN-LAST:event_formWindowOpened

