    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        String url = "jdbc:mysql://localhost:3306/";
        String dbName = "ad";
        String driver = "com.mysql.jdbc.Driver";
        String userName = "root";
        String password = "";
        try {
            Class.forName(driver).newInstance();
            Connection conn = DriverManager.getConnection(url + dbName, userName, password);
            Statement st = conn.createStatement();
            ResultSet adTable = st.executeQuery("Select adID, title from ad");
            final String selectedAd = comboAdTitle.getSelectedItem().toString();
            while (adTable.next()) {
                if (adTable.getString("title").equals(selectedAd)) {
                    final int selectedAdId = adTable.getInt("adID");
                    java.awt.EventQueue.invokeLater(new Runnable() {
                        public void run() {
                            frmModifyAd toOpen = new frmModifyAd();
                            toOpen.setAdID(selectedAdId);
                            toOpen.setVisible(true);
                        }
                    });
                    this.setVisible(false);
                    this.dispose();
                    break;
                }

            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSubmitMouseClicked

