    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost:3306/";
		  String dbName = "ad";
		  String driver = "com.mysql.jdbc.Driver";
		  String userName = "root"; 
		  String password = "";
		  try {
                      
                      fullName = comboSelectEmp.getSelectedItem().toString();
                      firstName = "";
                      for(int i = 0; i < fullName.length(); i++){
                          if(fullName.charAt(i) == ' '){
                              break;
                          }
                          firstName += fullName.charAt(i);
                      }
                      
                      Class.forName(driver).newInstance();
                      Connection conn = DriverManager.getConnection(url+dbName,userName,password);
                      Statement st= conn.createStatement();   
                      ResultSet ClientsTable=st.executeQuery("Select  empID, empFirstName, empLastName from employee");
                      
                      if(comboSelectEmp.getSelectedIndex()==0)
                      {
                         lblSelectEmp.setText("Please choose an employee");
                      }else{
                          while(ClientsTable.next())
                          {
                              if(ClientsTable.getString("empFirstName").equals(firstName))
                              {
                                  final frmModifyEmp toOpen=new frmModifyEmp();
                                  toOpen.setEmpID(ClientsTable.getInt("empID"));
                                  java.awt.EventQueue.invokeLater(new Runnable() {
                                    public void run() {
                                        toOpen.setVisible(true);
                                    }
                                });
                              }
                          }
                      }
                      conn.close();
		  } catch (Exception e) {
		  e.printStackTrace();
                  }
                  
                  this.setVisible(false);
                  this.dispose();
    }//GEN-LAST:event_btnSubmitMouseClicked

