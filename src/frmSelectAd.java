    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost:3306/";
        String dbName = "ad";
        String driver = "com.mysql.jdbc.Driver";
        String userName = "root";
        String password = "";

        try {
            Class.forName(driver).newInstance();
            Connection conn = DriverManager.getConnection(url + dbName, userName, password);
            Statement st = conn.createStatement();
            ResultSet ClientsTable = st.executeQuery("Select companyName, clientID from client");

            comboCompanyName.removeAllItems();
            comboCompanyName.addItem("            ");
            comboAdTitle.removeAllItems();
            comboAdTitle.addItem("            ");

            while (ClientsTable.next()) {

                comboCompanyName.addItem(ClientsTable.getString("companyName"));

            }

            comboCompanyName.addActionListener(new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    try {
                        String url = "jdbc:mysql://localhost:3306/";
                        String dbName = "ad";
                        String driver = "com.mysql.jdbc.Driver";
                        String userName = "root";
                        String password = "";
                        Class.forName(driver).newInstance();
                        Connection conn = DriverManager.getConnection(url + dbName, userName, password);
                        Statement st = conn.createStatement();
                        ResultSet clientTable = st.executeQuery("Select companyName, clientID from client");
                        int selectedClientID = 0;
                        while (clientTable.next()) {
                            if (comboCompanyName.getSelectedItem().toString().equals(clientTable.getString("companyName"))) {
                                selectedClientID = clientTable.getInt("clientID");
                                break;
                            }
                        }
                        ResultSet adTable = st.executeQuery("Select title from ad WHERE clientID = " + selectedClientID);
                        comboAdTitle.removeAllItems();
                        adTable.last();
                        int tableSize = adTable.getRow();
                        adTable.beforeFirst();
                        comboAdTitle.addItem("       ");
                        if (tableSize > 0) {
                            while (adTable.next()) {
                                comboAdTitle.addItem(adTable.getString("title"));
                            }
                        }
                    } catch (Exception ex) {
                        ex.printStackTrace();
                    }
                }
            });

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_formWindowOpened

